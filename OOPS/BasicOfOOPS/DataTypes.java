
public class DataTypes {
    public static void main(String[] args) {
        // Primitive Datatypes
        int a = 20;
        double b = 39;
        
        // Reference Datatypes
        String name = "Ravi";
        int arr[] = {1,2,3,6};
        DataTypes d = new DataTypes();

        System.out.println(a); // 20
        System.out.println(b); // 39.0
        
        System.out.println(name); // Ravi
        System.out.println(arr);  // [I@7ad041f3
        System.out.println(d);  // DataTypes@251a69d7
    }
}
